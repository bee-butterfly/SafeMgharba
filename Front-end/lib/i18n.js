export function useTranslation(lang) {
  return {
    t: (key, params) => {
      let text = translations[lang][key] || translations.en[key] || key

      if (params) {
        Object.entries(params).forEach(([param, value]) => {
          text = text.replace(`{${param}}`, String(value))
        })
      }

      return text
    },
  }
}
